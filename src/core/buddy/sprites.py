# ---------------------------------------------------------------------------
# Hat lines
# ---------------------------------------------------------------------------

HAT_LINES: dict[str, str] = {
    'none': '',
    'crown': '   \\^^^/    ',
    'tophat': '   [___]    ',
    'propeller': '    -+-     ',
    'halo': '   (   )    ',
    'wizard': '    /^\\     ',
    'beanie': '   (___)    ',
    'tinyduck': '    ,>      ',
}

# ---------------------------------------------------------------------------
# Render functions
# ---------------------------------------------------------------------------


def render_sprite(bones: CompanionBones, frame: int = 0) -> list[str]:
    """Render a sprite for the given companion bones and animation frame.

    Port of sprites.ts renderSprite().
    """
    frames = BODIES[bones.species]
    body = frames[frame % len(frames)]
    lines = [line.replace('{E}', bones.eye) for line in body]

    # Replace hat line if line 0 is blank and hat is not 'none'
    if bones.hat != 'none' and not lines[0].strip():
        lines[0] = HAT_LINES[bones.hat]

    # Drop blank hat slot if ALL frames have blank line 0
    if not lines[0].strip() and all(not f[0].strip() for f in frames):
        lines = lines[1:]

    return lines


def sprite_frame_count(species: str) -> int:
    return len(BODIES[species])


def render_face(bones: CompanionBones) -> str:
    """Render a compact one-line face for the companion.

    Port of sprites.ts renderFace().
    """
    e = bones.eye
    s = bones.species
    if s in ('duck', 'goose'):
        return f'({e}>)'
    if s == 'blob':
        return f'({e}{e})'
    if s == 'cat':
        return f'={e}\u03c9{e}='
    if s == 'dragon':
        return f'<{e}~{e}>'
    if s == 'octopus':
        return f'~({e}{e})~'
    if s == 'owl':
        return f'({e})({e})'
    if s == 'penguin':
        return f'({e}>)'
    if s == 'turtle':
        return f'[{e}_{e}]'
    if s == 'snail':
        return f'{e}(@)'
    if s == 'ghost':
        return f'/{e}{e}\\'
    if s == 'axolotl':
        return f'}}{e}.{e}{{'
    if s == 'capybara':
        return f'({e}oo{e})'
    if s == 'cactus':
        return f'|{e}  {e}|'
    if s == 'robot':
        return f'[{e}{e}]'
    if s == 'rabbit':
        return f'({e}..{e})'
    if s == 'mushroom':
        return f'|{e}  {e}|'
    if s == 'chonk':
        return f'({e}.{e})'
    if s == 'pikachu':
        return '⠸⣠⡶⠄⡠⢶⡄⠸'
    return f'({e}{e})'
